package output

const (
	// ANSI escape codes for colors.
	Reset = "\033[0m"
	Bold  = "\033[1m"

	// Basic Colors.
	Red    = "\033[31m"
	Blue   = "\033[34m"
	Cyan   = "\033[36m"
	Purple = "\033[35m"

	// Bright/Light Colors.
	LightBlue   = "\033[94m"
	LightCyan   = "\033[96m"
	LightPurple = "\033[95m"
)
